# ==================== 公共函数 ====================

def get_color_hex(name, system="all"):
    """
    根据英文名或中文名查找色值，支持所有色系。
    system: 可指定色系key（如"morandi"），"all"为全局查找。
    """
    if system == "all":
        for sys_dict in COLOR_SYSTEM.values():
            if isinstance(sys_dict, dict):
                for (en, cn), hexval in sys_dict.items():
                    if name == en or name == cn:
                        return hexval
    else:
        sys_dict = COLOR_SYSTEM.get(system)
        if isinstance(sys_dict, dict):
            for (en, cn), hexval in sys_dict.items():
                if name == en or name == cn:
                    return hexval
    return None

def get_color_system_name_from_en(en_name):
    """
    根据色系英文名获取中文名。
    """
    for (key, cn_name), sys_dict in COLOR_SYSTEM.items():
        if key == en_name:
            return cn_name
    return None

